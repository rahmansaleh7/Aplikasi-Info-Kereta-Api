package com.example.aplikasisubmissioniqbala.Model;

public class Kereta {
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKeterangan_kereta() {
        return keterangan_kereta;
    }

    public void setKeterangan_kereta(String keterangan_kereta) {
        this.keterangan_kereta = keterangan_kereta;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    private String nama, keterangan_kereta, photo;
}
